export const PERSONA_MAP = Object.fromEntries(PERSONAS.map(p => [p.id, p]))

export const PERSONAS_BY_TIER = {
  1: PERSONAS.filter(p => p.tier === 1),
  2: PERSONAS.filter(p => p.tier === 2),
  3: PERSONAS.filter(p => p.tier === 3),
  4: PERSONAS.filter(p => p.tier === 4),
}

export function getPersonasByDifficulty(difficulty) {
  if (difficulty >= 91) return PERSONAS_BY_TIER[4]
  if (difficulty >= 71) return PERSONAS_BY_TIER[3]
  if (difficulty >= 41) return PERSONAS_BY_TIER[2]
  return PERSONAS_BY_TIER[1]
}

export function getPersona(id) {
  return PERSONA_MAP[id] || PERSONAS[0]
}

export function getRandomPersona(tier) {
  const list = tier ? PERSONAS_BY_TIER[tier] : PERSONAS
  return list[Math.floor(Math.random() * list.length)]
}
